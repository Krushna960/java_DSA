
public class Basics {

    public static void main(String[] args) {

        int a = 10;
        if (a == 10) {
            System.out.println("a is 10");
        } else {
            System.out.println("a is not 10");

        }
        int count=1;
        while (count <= 5) {
            System.out.println("Count is: " + count);
            count++;
        }
        for (int i = 1; i <= 5; i++) {
            System.out.println("Iteration: " + i);
        }
    }
}
