public class Primitives {
    public static void main(String[] args) {
        // Example usage of primitive data types in Java
        int rollno = 64;
        java.lang.String name = "John";
        float fee = 5000.50f;// Using 'f' to denote float literal
        double percentage = 85.5;
        char grade = 'A'; 
        boolean isPassed = true;
        Long phoneNumber = 1234567890L;// Using 'L' to denote long literal
        

        System.out.println("Roll Number: " + rollno);
        System.out.println("Name: " + name);
        System.out.println("Fee: " + fee);
        System.out.println("Percentage: " + percentage);
        System.out.println("Grade: " + grade);
        System.out.println("Is Passed: " + isPassed);
        System.out.println("Phone Number: " + phoneNumber);
        
    }
}
